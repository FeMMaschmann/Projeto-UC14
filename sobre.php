  				<h2 class="meio">Sobre as esponjas</h2>
  				<p>As esponjas tambem podem ser chamados de poríferos ou Porífera.</p>
  				<p>Os poríferos ou Porífera (do latim porus, poro + phoros, portador de poros) é um filo do reino Animalia, sub-reino Parazoa, onde se enquadram os animais conhecidos como esponjas.<br>
            Estes organismos são simples, sésseis, podem ser de água doce ou salgada, alimentam-se por filtração, bombeando a água através das paredes do corpo e retendo as partículas de alimento nas suas células. As esponjas estão entre os animais mais simples, não possuem tecidos verdadeiros pois em sua camada externa e interna as células não apresentam lâmina basal (parazoas), também não apresentam músculos, sistema nervoso, nem órgãos internos. Eles são muito próximos a uma colônia celular de coanoflagelados, (o que mostra o provável salto evolutivo de unicelulares para pluricelulares) pois cada célula alimenta-se por si própria. Existem mais de 15 000 espécies modernas de esponjas conhecidas, que podem ser encontradas desde a superfície da água até mais de 8000 metros de profundidade, e muitas outras são descobertas a cada dia. O registro fóssil data as esponjas desde a era pré-cambriana (ou Pré-Câmbrico), ou Neoproterozoico.</p>
